/**
 * VerbNodeSeed — Data-driven spatial-node population loader (DOC-110 P3 Ch.0).
 *
 * Consumes `data/verb-nodes.json` (authored directly or via the future
 * BO-V verb-node layer, normalised by `tools/extract-verb-nodes.js`)
 * and registers every hand-authored node with VerbNodes via the public
 * `register(floorId, defs)` API.
 *
 * DEPLOYMENT NOTE
 * ===============
 * P3 Ch.0 SHIPPED 2026-04-17 — the inline ~150-line
 * `_registerBuiltinNodes()` block in engine/verb-nodes.js was retired.
 * data/verb-nodes.json is now the SOLE source of truth at runtime for
 * hand-authored floors (depth 1-2). Depth >=3 floors still auto-derive
 * via engine/dungeon-verb-nodes.js.
 *
 * If this module fails to load the JSON, a visible console warning
 * fires and VerbNodes stays empty — no silent fallback. The pattern
 * mirrors engine/npc-seed.js (Phase 0 Ch.5 cutover).
 *
 * Sync XHR matches NpcSeed / QuestRegistry conventions. A .js sidecar
 * (data/verb-nodes.js, generated by extract-verb-nodes.js) is loaded
 * BEFORE this module — we prefer the in-memory `window.VERB_NODES_DATA`
 * snapshot and fall back to XHR only if the sidecar didn't load.
 *
 * Layer 1 — depends on VerbNodes (Layer 1) being loaded first.
 */
var VerbNodeSeed = (function () {
  'use strict';

  var _data        = null;  // Raw parsed envelope
  var _didPopulate = false;

  /**
   * Load data/verb-nodes.json. Prefers the sidecar snapshot
   * (window.VERB_NODES_DATA) so the load works under file:// on
   * Chromium without CORS friction; falls back to sync XHR for
   * Node / harness contexts.
   *
   * @returns {boolean} true on success, false on miss or parse error.
   */
  function load() {
    // Preferred path: sidecar script already populated the global.
    if (typeof window !== 'undefined' && window.VERB_NODES_DATA) {
      _data = window.VERB_NODES_DATA;
      return true;
    }
    // Fallback: sync XHR (same pattern as NpcSeed).
    try {
      if (typeof XMLHttpRequest === 'undefined') return false;
      var xhr = new XMLHttpRequest();
      xhr.open('GET', 'data/verb-nodes.json', false);
      xhr.send(null);
      if (xhr.status !== 200 && xhr.status !== 0) {
        console.warn('[VerbNodeSeed] data/verb-nodes.json not fetchable (status '
          + xhr.status + '). VerbNodes will be empty for hand-authored floors.');
        return false;
      }
      _data = JSON.parse(xhr.responseText);
      return true;
    } catch (e) {
      console.warn('[VerbNodeSeed] Load failed: ' + (e && e.message) +
        '. VerbNodes will be empty for hand-authored floors.');
      _data = null;
      return false;
    }
  }

  /**
   * Whitelist a single node to the runtime shape VerbNodes consumes.
   * Unknown keys drop; schema-enforced fields pass through verbatim.
   */
  function _toRuntimeDef(jsonNode) {
    var def = {
      id:   jsonNode.id,
      type: jsonNode.type,
      x:    jsonNode.x,
      y:    jsonNode.y
    };
    if (jsonNode.faction != null)   def.faction   = jsonNode.faction;
    if (jsonNode.contested === true) def.contested = true;
    return def;
  }

  /**
   * Register every node in the loaded envelope with VerbNodes.
   * Idempotent — calling twice is a no-op after the first success.
   *
   * @returns {Object} { ok, floorCount, nodeCount, skipped }
   */
  function populate() {
    if (_didPopulate) {
      return { ok: true, floorCount: 0, nodeCount: 0, skipped: 'already-populated' };
    }
    if (!_data) {
      if (!load()) {
        return { ok: false, floorCount: 0, nodeCount: 0, skipped: 'load-failed' };
      }
    }
    if (typeof VerbNodes === 'undefined' || !VerbNodes.register) {
      return { ok: false, floorCount: 0, nodeCount: 0, skipped: 'verb-nodes-missing' };
    }

    var byFloor = _data.nodesByFloor || {};
    var floorKeys = Object.keys(byFloor);
    var total = 0;
    for (var i = 0; i < floorKeys.length; i++) {
      var fid = floorKeys[i];
      var list = (byFloor[fid] || []).map(_toRuntimeDef);
      if (list.length > 0) {
        VerbNodes.register(fid, list);
        total += list.length;
      }
    }
    _didPopulate = true;
    console.log('[VerbNodeSeed] Populated ' + total + ' spatial node(s) across '
      + floorKeys.length + ' floor(s) from data/verb-nodes.json');
    return { ok: true, floorCount: floorKeys.length, nodeCount: total };
  }

  /** Reset — exposed for test harnesses only. */
  function reset() { _data = null; _didPopulate = false; }

  /** Peek at the loaded manifest without registering. */
  function manifest() { return _data; }

  // Auto-populate on load. VerbNodes itself is a zero-dep IIFE so it's
  // already available by the time this script tag runs (load order
  // in index.html guarantees engine/verb-nodes.js → sidecar → this).
  var _result = populate();
  if (!_result.ok) {
    console.warn('[VerbNodeSeed] Initial populate failed: ' + (_result.skipped || 'unknown') +
      '. VerbNodes registry is empty for hand-authored floors.');
  }

  return Object.freeze({
    load:     load,
    populate: populate,
    reset:    reset,
    manifest: manifest
  });
})();
