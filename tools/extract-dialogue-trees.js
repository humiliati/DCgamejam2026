#!/usr/bin/env node
/**
 * extract-dialogue-trees.js
 *
 * Extracts dialogue trees from engine/npc-dialogue-trees.js into
 * data/dialogue-trees.json. Part of the DOC-110 Phase 2b dialogue
 * tree data extraction (mirrors the NPC Phase 0 cutover pattern).
 *
 * Usage:
 *   node tools/extract-dialogue-trees.js
 *
 * Output:
 *   data/dialogue-trees.json — canonical JSON source of truth
 *   data/dialogue-trees.js   — browser sidecar (window.DIALOGUE_TREES_DATA)
 */
'use strict';

var fs   = require('fs');
var path = require('path');

var ROOT = path.resolve(__dirname, '..');
var SRC  = path.join(ROOT, 'engine', 'npc-dialogue-trees.js');
var OUT  = path.join(ROOT, 'data', 'dialogue-trees.json');
var SIDE = path.join(ROOT, 'data', 'dialogue-trees.js');

// ── Shim NpcSystem to capture registerTree calls ────────────────

var _captured = [];

global.NpcSystem = {
  registerTree: function(id, tree) {
    _captured.push({ id: id, tree: tree });
  }
};

// Stubs for dynamic root eval
global.Player = {
  getFlag: function() { return false; },
  state: function() { return {}; }
};

// ── Load and execute the dialogue trees module ──────────────────
// The IIFE assigns to `var NpcDialogueTrees` which eval in strict
// mode won't leak, so we use Function() constructor or require-from-string.
// Simplest: vm.runInThisContext which shares the global scope.

var vm = require('vm');
var src = fs.readFileSync(SRC, 'utf8');
vm.runInThisContext(src, { filename: SRC });

if (typeof NpcDialogueTrees !== 'undefined' && NpcDialogueTrees.registerAll) {
  NpcDialogueTrees.registerAll();
}

// ── Convert captured trees to JSON-serialisable format ──────────

var output = {
  _meta: {
    generated: new Date().toISOString(),
    source: 'engine/npc-dialogue-trees.js',
    schema: 'tools/dialogue-tree-schema.json',
    treeCount: _captured.length
  },
  trees: {}
};

for (var i = 0; i < _captured.length; i++) {
  var entry = _captured[i];
  var id    = entry.id;
  var tree  = entry.tree;

  var treeData = {
    root: null,
    nodes: {}
  };

  // Handle root — static string or dynamic conditional
  if (typeof tree.root === 'function') {
    // Parse the dynamic root function to extract condition
    // Known pattern: Player.getFlag('flagName') ? 'rootA' : 'rootB'
    var fnStr = tree.root.toString();
    var condMatch = fnStr.match(/getFlag\(['"]([^'"]+)['"]\)\s*\)\s*\?\s*['"]([^'"]+)['"]\s*:\s*['"]([^'"]+)['"]/);
    if (condMatch) {
      treeData.root = {
        condition: { flag: condMatch[1] },
        ifTrue: condMatch[2],
        ifFalse: condMatch[3]
      };
    } else {
      // Fallback: store as 'greeting' with a warning
      console.warn('  WARNING: Could not parse dynamic root for ' + id + '. Using "greeting".');
      treeData.root = 'greeting';
    }
  } else {
    treeData.root = tree.root;
  }

  // Convert nodes
  for (var nodeId in tree.nodes) {
    if (!tree.nodes.hasOwnProperty(nodeId)) continue;
    var node = tree.nodes[nodeId];
    var nodeData = {
      text: node.text
    };

    if (node.choices && node.choices.length > 0) {
      nodeData.choices = [];
      for (var c = 0; c < node.choices.length; c++) {
        var choice = node.choices[c];
        var choiceData = {
          label: choice.label,
          next: choice.next
        };
        if (choice.effect) {
          choiceData.effect = {};
          for (var ek in choice.effect) {
            if (choice.effect.hasOwnProperty(ek)) {
              choiceData.effect[ek] = choice.effect[ek];
            }
          }
        }
        if (choice.showIf) {
          choiceData.showIf = {};
          for (var sk in choice.showIf) {
            if (choice.showIf.hasOwnProperty(sk)) {
              choiceData.showIf[sk] = choice.showIf[sk];
            }
          }
        }
        nodeData.choices.push(choiceData);
      }
    }

    treeData.nodes[nodeId] = nodeData;
  }

  output.trees[id] = treeData;
}

// ── Write JSON ──────────────────────────────────────────────────

var json = JSON.stringify(output, null, 2);
fs.writeFileSync(OUT, json + '\n', 'utf8');

// ── Write browser sidecar ───────────────────────────────────────

var sidecar = '/**\n' +
  ' * dialogue-trees.js — browser sidecar for data/dialogue-trees.json\n' +
  ' * AUTO-GENERATED by tools/extract-dialogue-trees.js — do not hand-edit.\n' +
  ' * ' + new Date().toISOString() + '\n' +
  ' */\n' +
  'var DIALOGUE_TREES_DATA = ' + json + ';\n';

fs.writeFileSync(SIDE, sidecar, 'utf8');

// ── Report ──────────────────────────────────────────────────────

var totalNodes = 0;
var totalChoices = 0;
var ids = Object.keys(output.trees);

for (var t = 0; t < ids.length; t++) {
  var tr = output.trees[ids[t]];
  var nc = Object.keys(tr.nodes).length;
  var cc = 0;
  for (var nid in tr.nodes) {
    cc += (tr.nodes[nid].choices || []).length;
  }
  totalNodes += nc;
  totalChoices += cc;
}

console.log('[extract-dialogue-trees] Extracted ' + ids.length + ' trees (' +
  totalNodes + ' nodes, ' + totalChoices + ' choices) → ' + OUT);
console.log('[extract-dialogue-trees] Browser sidecar → ' + SIDE);
