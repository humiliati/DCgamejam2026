#!/usr/bin/env node
/**
 * generate-verb-node-overrides-schema-sidecar.js — DOC-110 P3 Ch.2 stretch.
 *
 * Emits tools/verb-node-overrides-schema.js alongside the JSON so
 * browser-based tooling (future BO-V override panel, validation
 * previews) can load the schema under file://.
 *
 * Mirrors tools/generate-verb-node-schema-sidecar.js. Idempotent.
 *
 * Usage:  node tools/generate-verb-node-overrides-schema-sidecar.js
 */
'use strict';

var fs   = require('fs');
var path = require('path');

var ROOT     = path.resolve(__dirname, '..');
var SRC_JSON = path.join(ROOT, 'tools', 'verb-node-overrides-schema.json');
var OUT_JS   = path.join(ROOT, 'tools', 'verb-node-overrides-schema.js');

function main() {
  var raw = fs.readFileSync(SRC_JSON, 'utf8');
  var schema = JSON.parse(raw);
  var pretty = JSON.stringify(schema, null, 2);

  var header =
    '// AUTO-GENERATED by tools/generate-verb-node-overrides-schema-sidecar.js — do not edit by hand.\n' +
    '// Sidecar wrapper so browser tooling can load the schema under file://\n' +
    '// (bypasses Chromium CORS fetch block). Keep in sync with\n' +
    '// tools/verb-node-overrides-schema.json. Consumed via window.VERB_NODE_OVERRIDES_SCHEMA.\n' +
    'window.VERB_NODE_OVERRIDES_SCHEMA = ';

  var body = header + pretty + ';\n';
  fs.writeFileSync(OUT_JS, body, 'utf8');

  var defCount = Object.keys(schema.definitions || {}).length;
  console.log('[verb-node-overrides-schema-sidecar] wrote ' + path.relative(ROOT, OUT_JS) +
    ' (' + defCount + ' definitions, ' + body.length + ' bytes)');
}

main();
