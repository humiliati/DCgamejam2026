#!/usr/bin/env node
/**
 * generate-verb-node-template-sidecar.js — DOC-110 P3 Ch.1.
 *
 * Emits tools/verb-node-templates.js alongside tools/verb-node-templates.json
 * so browser-based tooling (future BO-V verb-node layer — P3 Ch.2) can load
 * templates under file:// where Chromium blocks fetch()-of-JSON.
 *
 * Mirrors tools/generate-archetype-sidecar.js (archetype registry).
 *
 * Run after every edit to tools/verb-node-templates.json. Idempotent.
 *
 * Usage:  node tools/generate-verb-node-template-sidecar.js
 */
'use strict';

var fs   = require('fs');
var path = require('path');

var ROOT     = path.resolve(__dirname, '..');
var SRC_JSON = path.join(ROOT, 'tools', 'verb-node-templates.json');
var OUT_JS   = path.join(ROOT, 'tools', 'verb-node-templates.js');

function main() {
  var raw = fs.readFileSync(SRC_JSON, 'utf8');
  var payload = JSON.parse(raw);

  // Deterministic templateCount regen so the sidecar never drifts
  // from the actual count even if someone hand-edits the meta.
  if (payload && payload._meta && Array.isArray(payload.templates)) {
    payload._meta.templateCount = payload.templates.length;
    payload._meta.generatedAt   = new Date().toISOString();
  }

  var pretty = JSON.stringify(payload, null, 2);

  var header =
    '// AUTO-GENERATED by tools/generate-verb-node-template-sidecar.js — do not edit by hand.\n' +
    '// Sidecar wrapper so browser tooling can load templates under file://\n' +
    '// (bypasses Chromium CORS fetch block). Keep in sync with\n' +
    '// tools/verb-node-templates.json. Consumed via window.VERB_NODE_TEMPLATES.\n' +
    '//\n' +
    '// Also re-emits the source JSON with a refreshed generatedAt + templateCount\n' +
    '// so the two files never drift. Node layer only — browsers ignore this path.\n' +
    'window.VERB_NODE_TEMPLATES = ';

  var body = header + pretty + ';\n';
  fs.writeFileSync(OUT_JS, body, 'utf8');
  // Re-emit the JSON with refreshed _meta (idempotent aside from timestamp)
  fs.writeFileSync(SRC_JSON, pretty + '\n', 'utf8');

  var templateCount = (payload.templates || []).length;
  var nodeCount = (payload.templates || []).reduce(function (acc, t) {
    return acc + ((t.nodes && t.nodes.length) || 0);
  }, 0);
  console.log('[verb-node-template-sidecar] wrote ' + path.relative(ROOT, OUT_JS) +
    ' (' + templateCount + ' templates, ' + nodeCount + ' nodes, ' + body.length + ' bytes)');
}

main();
