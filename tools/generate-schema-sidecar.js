#!/usr/bin/env node
/**
 * generate-schema-sidecar.js — DOC-110 Phase 1.1.
 *
 * Emits tools/actor-schema.js alongside tools/actor-schema.json so that
 * browser-based tooling (tools/npc-designer.html) can load the schema
 * under file:// where Chromium blocks fetch()-of-JSON.
 *
 * The sidecar is a 1-line IIFE header wrapping the parsed JSON and
 * attaching it to window.ACTOR_SCHEMA. Mirrors the pattern used by
 * data/npcs.js (see tools/extract-npcs.js).
 *
 * Run after every edit to tools/actor-schema.json. Idempotent.
 *
 * Usage:  node tools/generate-schema-sidecar.js
 */
'use strict';

var fs   = require('fs');
var path = require('path');

var ROOT      = path.resolve(__dirname, '..');
var SRC_JSON  = path.join(ROOT, 'tools', 'actor-schema.json');
var OUT_JS    = path.join(ROOT, 'tools', 'actor-schema.js');

function main() {
  var raw = fs.readFileSync(SRC_JSON, 'utf8');
  // Parse to validate + re-serialise with stable indentation.
  var schema = JSON.parse(raw);
  var pretty = JSON.stringify(schema, null, 2);

  var header =
    '// AUTO-GENERATED by tools/generate-schema-sidecar.js — do not edit by hand.\n' +
    '// Sidecar wrapper so tools/npc-designer.html can load the schema\n' +
    '// under file:// (bypasses Chromium CORS fetch block).\n' +
    '// Keep in sync with tools/actor-schema.json.\n' +
    '// Consumed by tools/schema-validator.js via window.ACTOR_SCHEMA.\n' +
    'window.ACTOR_SCHEMA = ';

  var body = header + pretty + ';\n';

  fs.writeFileSync(OUT_JS, body, 'utf8');

  var defCount = Object.keys(schema.definitions || {}).length;
  console.log('[schema-sidecar] wrote ' + path.relative(ROOT, OUT_JS) +
    ' (' + defCount + ' definitions, ' + body.length + ' bytes)');
}

main();
